    boolean isValidate(){
        // check ko được bỏ trống 
        if(txtMaChuyenDe.getText().isEmpty()){
        MsgBox.alert(this, "Không cho phép để trống mã");
        return false;
        }else if(txtTenChuyenDe.getText().isEmpty()){
        MsgBox.alert(this, "Không cho phép để trống tên");
        return false;
        }else{
            //check ko được viết chữ
            try{
                int so =Integer.parseInt(txtThoiLuong.getText());
                double so1 = Double.parseDouble(txtHocPhi.getText());
                //check ko được số âm
                if(so < 0) {
                MsgBox.alert(this, "không cho phép số âm");                    
                }
                if(so1 < 0) {
                MsgBox.alert(this, "không cho phép số âm");                    
                }
            }catch(Exception e){
                MsgBox.alert(this, "Không cho phép nhập chữ");
                return false;
            }
        }
        return true;
    }
