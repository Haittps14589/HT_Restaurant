    void insert(){
        ChuyenDe cd = getForm();
            try{
                dao.insert(cd);this.fillTable(); this.clearForm();
                MsgBox.alert(this, "Thêm mới thành công!");
            }catch(Exception e){
                e.printStackTrace();
                MsgBox.alert(this, "Thêm mới thất bại!");
            }
    }
    void update(){
        ChuyenDe cd = getForm();
            try{
                dao.update(cd); this.fillTable();
                MsgBox.alert(this, "Cập nhật thành công!");
            }catch(Exception e){
                MsgBox.alert(this, "Cập nhật thất bại");
            }
        
    }
    void delete(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không có quyền xóa Chuyên đề!");
        }else{
            String macd = txtMaChuyenDe.getText();
            if(MsgBox.confirm(this, "Bạn thực sự muốn xóa chuyên đề này?")){
                try{
                    dao.delete(macd);this.fillTable();this.clearForm();
                    MsgBox.alert(this, "xóa thành công");
                }catch(Exception e){
                    MsgBox.alert(this, "Xóa thất bại");
                }
            }
        }
    }
    void clearForm(){
        ChuyenDe cd = new ChuyenDe();
        this.setForm(cd);
        this.row = -1;
        this.updateStatus();
    }
    void edit(){
        if(row < 0) return;
        String macd = (String) tblChuyenDe.getValueAt(row, 0);
        ChuyenDe cd = dao.selectById(macd);
        this.setForm(cd);
        tabs.setSelectedIndex(0);
        this.updateStatus();
    }
    void first(){
        this.row = 0;
        this.edit();
    }
    void prev(){
        if(this.row > 0){
            this.row--;
            this.edit();
        }
    }
    void next(){
        if(this.row < tblChuyenDe.getRowCount()-1){
            this.row++;
            this.edit();
        }
    }
    void last(){
        this.row = tblChuyenDe.getRowCount()-1;
        this.edit();
    }
    void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblChuyenDe.getModel();
        model.setRowCount(0);
        try{
            List<ChuyenDe> list = dao.selectAll();
            for(ChuyenDe cd : list){
                Object[] row = {cd.getMaCD(), cd.getTenCD(),cd.getHocPhi(),cd.getThoiLuong(),cd.getHinh()};
                model.addRow(row);
            };
            
        }catch(Exception e){
            e.printStackTrace();
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }
    void setForm(ChuyenDe cd){
        txtMaChuyenDe.setText(cd.getMaCD());
        txtTenChuyenDe.setText(cd.getTenCD());
        txtThoiLuong.setText(String.valueOf(cd.getThoiLuong()));
        txtHocPhi.setText(String.valueOf(cd.getHocPhi()));
        txtMota.setText(cd.getMoTa());
        if(cd.getHinh()!= null){
            ImageIcon icon = XImage.read(cd.getHinh());       
            Image im = icon.getImage();
            ImageIcon iconn = new ImageIcon(im.getScaledInstance(lblHinhAnh.getWidth(), lblHinhAnh.getHeight(), im.SCALE_SMOOTH));
            lblHinhAnh.setIcon(iconn);
            lblHinhAnh.setToolTipText(cd.getHinh());
        }
    }
    ChuyenDe getForm(){
        ChuyenDe cd = new ChuyenDe();
        cd.setMaCD(txtMaChuyenDe.getText());
        cd.setTenCD(txtTenChuyenDe.getText());
        cd.setThoiLuong(Integer.valueOf(txtThoiLuong.getText()));
        cd.setHocPhi(Double.valueOf(txtHocPhi.getText()));
        cd.setMoTa(txtMota.getText());
        if(lblHinhAnh.getToolTipText() != null){
        cd.setHinh(lblHinhAnh.getToolTipText());
        }else{
            cd.setHinh("noAvatar.png");
        }
        return cd;
    }
    void updateStatus(){
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblChuyenDe.getRowCount()-1);
        // Trang Thai From
        txtMaChuyenDe.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        //Trang thai dieu huong
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }
 
